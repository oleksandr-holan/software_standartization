from __future__ import annotations

import streamlit as st

# (коротка назва, повна назва, пояснення, формула або норма)
Entry = tuple[str, str, str, str]


def render_legend(title: str, entries: list[Entry], *, expanded: bool = False) -> None:
    with st.expander(title, expanded=expanded):
        rows = [
            {
                "Скорочення": e[0],
                "Повна назва": e[1],
                "Що означає": e[2],
                "Формула / норма": e[3],
            }
            for e in entries
        ]
        st.dataframe(rows, use_container_width=True, hide_index=True)


def input_label(short: str, full: str) -> str:
    return f"{short} — {full}"


