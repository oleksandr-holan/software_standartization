def mood_number_input(
    column,
    key: str,
    mapping: dict[str, Entry],
    session_key: str,
    default: int,
    *,
    min_val: int = 0,
    max_val: int = 1000,
) -> int:
    short, full, explain, _formula = mapping[key]
    return column.number_input(
        input_label(short, full),
        min_val,
        max_val,
        int(st.session_state.get(session_key, default)),
        help=f"{explain} Формула: {_formula}." if _formula else explain,
    )
